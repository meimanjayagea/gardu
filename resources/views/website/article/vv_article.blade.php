                            berpuasa serta tidak melanggar perintah Tuhan lagi. ( II Samuel 12 : 7-25 ).</p>

                            <p class="text-justify">Sama-sama memiliki masalah, tapi bila salah mencari solusi maka hasilnya beda. Raja Saul
