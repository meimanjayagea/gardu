                            dalam mencari solusi. Saat dia dan tentara Israel menghadapi tentara Filistin, Saul takut
                            sekali karena Roh Tuhan telah undur darinya. Dia pergi mencari solusi dengan datang ke Endor
                            ketemu seorang ahli roh tenung dan memanggil arwah. Iman yang kuat bisa hilang saat Tuhan
